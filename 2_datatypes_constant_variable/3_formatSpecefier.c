

#include <stdio.h>

int main()
{

    // integer=> %d

    int num = 10;

    printf("%d\n", num);

    // float =>%f

    float floatingNum = 99.99;

    printf("%f\n", floatingNum);

    // char => %c

    char singleCharacter = 'a';

    printf("%c\n", singleCharacter);

    // string => %s

    char string[] = "this is textual data";

    printf("%s\n", string);

    return 0;
}